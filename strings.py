# split line#
print("Giraffe\nAcademy")

# set variable#
phrase = "Giraffe Academy"

# change to all lower#
print(phrase.lower())
# change to all upper
print(phrase.upper())

# pass if upper or lower (boolean)#
print(phrase.isupper())
print(phrase.islower())

# Change to upper + passing is upper case?#
print(phrase.upper().isupper())

# Change to lower + passing is lower case?#
print(phrase.lower().lower())

# find length#
print(len(phrase))

# index - where is X#
print(phrase.index("Aca"))

# pull characters (zero index and selected character#
print(phrase[0])

# replace Giraffe with kitten#
print(phrase.replace("Giraffe", "Kitten"))
print(phrase.upper)
